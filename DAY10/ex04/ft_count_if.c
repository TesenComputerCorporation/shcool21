int	ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int r;

	i = 0;
	r = 0;
	while (tab[i])
	{
		r = r + ft_count_if(tab, f(tab[i]));
		++i;
	}
	return (r);
}
