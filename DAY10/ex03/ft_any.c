int	ft_any(char **tab, int (*f)(char*))
{
	int i;
	int r;

	i = 0;
	r = 0;
	while (tab[i])
	{
		r = ft_any(tab, f(tab[i]));
		++i;
	}
	if (r > 0)
		return (1);
	return (0);
}
